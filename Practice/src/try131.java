
public class try131 {
	public static void main(String[] args) {
		System.out.println(solve("bracadabra", 3));

	}

	public static String solve(String s, int k) {
		char c = 'a';
		while (k > 0) {
			for (int i = 0; i < s.length(); i++) {
				if (s.charAt(i) == c)
					;
			}

		}
		return "";
	}
}
